#include "libft.h"

int	ft_rev(void *s, int x, int y, size_t size)
{
	if (x >= --y)
		return (0);
	while (x < y)
	{
		if (!(ft_swap(s + (size * x), s + (size * y), size)))
			return (0);
		x++;
		y--;
	}
	return (1);
}
